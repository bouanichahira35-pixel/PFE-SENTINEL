export function getDemandeurI18n(language) {
  return DEMANDEUR_I18N[language] || DEMANDEUR_I18N.fr;
}

export function formatDemandeurText(template, values = {}) {
  return String(template || '').replace(/\{(\w+)\}/g, (_, key) => (
    Object.prototype.hasOwnProperty.call(values, key) ? String(values[key]) : ''
  ));
}
